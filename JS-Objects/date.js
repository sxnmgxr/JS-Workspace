/*
the Date object in JavaScript.
The Date object is a built-in object in JavaScript that allows
developers to work with dates and times. It provides a way to
create and manipulate dates and times, and perform operations
on them. The Date object is very useful in web development
when you need to display, process or manipulate dates and
times.
*/

const today = new Date();

console.log(today);

/*
The Date object provides a number of methods that allow you to
perform operations on dates and times. Some of the most
commonly used methods include:
*/

console.log(today.getFullYear); //year

console.log(today.getMonth); //month

console.log(today.getDay);

console.log(today.getMilliseconds);

console.log(today.getSeconds);
